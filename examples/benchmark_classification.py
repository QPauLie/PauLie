"""Benchmark: brute-force Lie closure vs PauLie's graph-based classification.

This script compares two approaches to determining the dynamical Lie algebra (DLA)
generated by a set of Pauli string generators:

1. **Brute-force Lie closure** — Iteratively compute commutators until the algebra
   closes. This is the approach used by kak-tools' ``lie_closure_pauli_words``.
   Runtime scales with the *size of the algebra* (exponential for su/sp types).

2. **PauLie classification** — Use anti-commutation graph structure to classify the
   algebra in polynomial time, without ever enumerating algebra elements.

For so(n) algebras (dim ~ n^2), brute force is manageable. For su(2^n) or sp(2^n)
algebras (dim ~ 4^n), brute force becomes infeasible while PauLie remains fast.

Usage::

    uv run python examples/benchmark_classification.py           # tables only
    uv run python examples/benchmark_classification.py --plot    # tables + plots
"""

import argparse
import random
import signal
import time
from dataclasses import dataclass, field
from itertools import product

from paulie import (
    G_LIE,
    PauliString,
    PauliStringCollection,
    get_pauli_string as p,
)


# ---------------------------------------------------------------------------
# Brute-force Lie closure
# ---------------------------------------------------------------------------

class _BruteForceTimeout(Exception):
    """Raised when brute-force exceeds time limit."""


def _timeout_handler(signum, frame):
    raise _BruteForceTimeout()


def lie_closure_brute_force(generators: list[PauliString],
                            timeout: float = 30.0) -> list[PauliString]:
    """Compute the Lie closure by brute-force commutator enumeration.

    This is equivalent to kak-tools' ``lie_closure_pauli_words`` but uses
    PauLie's native PauliString type instead of PennyLane's PauliWord.

    Args:
        generators: Initial generator set.
        timeout: Maximum wall-clock seconds before aborting.

    Returns:
        All elements of the closed Pauli Lie algebra.

    Raises:
        _BruteForceTimeout: If the computation exceeds the timeout.
    """
    signal.signal(signal.SIGALRM, _timeout_handler)
    signal.alarm(int(timeout) + 1)

    try:
        dla = list(generators)
        old_length = 0
        new_length = len(dla)
        initial_length = new_length

        while new_length > old_length:
            for pw1, pw2 in product(dla[:initial_length], dla[old_length:]):
                if pw1.commutes_with(pw2):
                    continue
                com = pw1 @ pw2
                if com not in dla:
                    dla.append(com)

            old_length = new_length
            new_length = len(dla)

        return dla
    finally:
        signal.alarm(0)


# ---------------------------------------------------------------------------
# Result collection
# ---------------------------------------------------------------------------

@dataclass
class BenchmarkResult:
    """Result of a single benchmark run."""

    n: int
    algebra: str
    dla_dim: int
    t_paulie: float
    t_brute: float | None
    timed_out: bool


@dataclass
class BenchmarkSection:
    """A named section of benchmark results for plotting."""

    title: str
    short_label: str
    results: list[BenchmarkResult] = field(default_factory=list)


# ---------------------------------------------------------------------------
# Helpers
# ---------------------------------------------------------------------------

def format_time(t: float) -> str:
    """Format time in human-readable form."""
    if t < 0.001:
        return f"{t*1e6:.0f}us"
    if t < 1:
        return f"{t*1e3:.1f}ms"
    if t < 60:
        return f"{t:.2f}s"
    return f"{t/60:.1f}min"


def print_header() -> None:
    """Print table header."""
    print(
        f"  {'n':>3}  {'algebra':>20}  {'dim':>8}  "
        f"{'PauLie':>10}  {'brute-force':>12}  {'speedup':>10}",
        flush=True,
    )
    print("  " + "-" * 75, flush=True)


def make_heisenberg_generators(n: int) -> PauliStringCollection:
    """Build XX+YY couplings + Z fields on n qubits."""
    gens = []
    for i in range(n - 1):
        xx = ["I"] * n
        xx[i], xx[i + 1] = "X", "X"
        gens.append("".join(xx))
        yy = ["I"] * n
        yy[i], yy[i + 1] = "Y", "Y"
        gens.append("".join(yy))
    for i in range(n):
        z = ["I"] * n
        z[i] = "Z"
        gens.append("".join(z))
    return p(gens)


def make_random_pauli_hamiltonian(n: int, n_terms: int,
                                  locality: int = 2,
                                  seed: int | None = None) -> PauliStringCollection:
    """Build a random Pauli Hamiltonian with k-local terms.

    Args:
        n: Number of qubits.
        n_terms: Number of generator terms.
        locality: Maximum locality of each term.
        seed: Random seed for reproducibility.

    Returns:
        PauliStringCollection of random generators.
    """
    rng = random.Random(seed)
    paulis = "XYZ"
    gens = set()
    while len(gens) < n_terms:
        s = ["I"] * n
        k = rng.randint(1, min(locality, n))
        positions = rng.sample(range(n), k)
        for pos in positions:
            s[pos] = rng.choice(paulis)
        gens.add("".join(s))
    return p(list(gens))


# ---------------------------------------------------------------------------
# Benchmark runner
# ---------------------------------------------------------------------------

def run_section(title: str, short_label: str,
                cases: list[tuple[int, PauliStringCollection]],
                bf_timeout: float = 15.0,
                bf_max_n: int | None = None) -> BenchmarkSection:
    """Run a benchmark section, print results, and return collected data.

    Args:
        title: Section title for printing.
        short_label: Short label for plot legend.
        cases: List of (n_qubits, generators) pairs.
        bf_timeout: Timeout for brute-force per case.
        bf_max_n: Skip brute-force for n > this value.

    Returns:
        BenchmarkSection with all results.
    """
    section = BenchmarkSection(title=title, short_label=short_label)
    print(f"\n{title}", flush=True)
    print_header()

    for n, generators in cases:
        t0 = time.perf_counter()
        algebra = generators.get_algebra()
        dla_dim = generators.get_dla_dim()
        t_paulie = time.perf_counter() - t0

        skip_bf = bf_max_n is not None and n > bf_max_n
        t_brute = None
        timed_out = False

        if skip_bf:
            bf_str = "INFEASIBLE"
            speedup_str = "-"
        else:
            t0 = time.perf_counter()
            try:
                dla = lie_closure_brute_force(generators.get(), timeout=bf_timeout)
                t_brute = time.perf_counter() - t0
                bf_str = f"{format_time(t_brute)} ({len(dla)})"
                speedup = t_brute / t_paulie if t_paulie > 1e-9 else float("inf")
                speedup_str = f"{speedup:.0f}x"
            except _BruteForceTimeout:
                t_brute = time.perf_counter() - t0
                timed_out = True
                bf_str = "TIMEOUT"
                speedup_str = f">{t_brute / t_paulie:.0f}x"

        section.results.append(BenchmarkResult(
            n=n, algebra=algebra, dla_dim=dla_dim,
            t_paulie=t_paulie, t_brute=t_brute, timed_out=timed_out,
        ))

        print(
            f"  {n:>3}  {algebra:>20}  {dla_dim:>8}  "
            f"{format_time(t_paulie):>10}  {bf_str:>12}  {speedup_str:>10}",
            flush=True,
        )

    return section


# ---------------------------------------------------------------------------
# Plotting
# ---------------------------------------------------------------------------

def plot_results(sections: list[BenchmarkSection]) -> None:
    """Generate runtime-vs-system-size plots.

    Args:
        sections: List of completed benchmark sections.
    """
    import matplotlib.pyplot as plt  # pylint: disable=import-outside-toplevel

    # --- Figure 1: PauLie runtime across all algebra types ---
    fig1, ax1 = plt.subplots(figsize=(10, 6))
    for sec in sections:
        ns = [r.n for r in sec.results]
        ts = [r.t_paulie for r in sec.results]
        ax1.plot(ns, ts, "o-", label=sec.short_label, markersize=5)

    ax1.set_xlabel("Number of qubits (n)", fontsize=12)
    ax1.set_ylabel("Runtime (seconds)", fontsize=12)
    ax1.set_title("PauLie classification runtime vs system size", fontsize=14)
    ax1.set_yscale("log")
    ax1.legend(fontsize=9, loc="upper left")
    ax1.grid(True, alpha=0.3)
    fig1.tight_layout()
    fig1.savefig("benchmark_paulie_runtime.pdf", dpi=150)
    fig1.savefig("benchmark_paulie_runtime.png", dpi=150)
    print("\nSaved: benchmark_paulie_runtime.pdf/.png", flush=True)

    # --- Figure 2: PauLie vs brute-force comparison (sections with BF data) ---
    sections_with_bf = [s for s in sections
                        if any(r.t_brute is not None for r in s.results)]
    n_plots = len(sections_with_bf)
    if n_plots == 0:
        return

    cols = min(3, n_plots)
    rows = (n_plots + cols - 1) // cols
    fig2, axes = plt.subplots(rows, cols, figsize=(5 * cols, 4.5 * rows), squeeze=False)

    for idx, sec in enumerate(sections_with_bf):
        ax = axes[idx // cols][idx % cols]

        ns_paulie = [r.n for r in sec.results]
        ts_paulie = [r.t_paulie for r in sec.results]
        ax.plot(ns_paulie, ts_paulie, "s-", color="tab:blue",
                label="PauLie", markersize=6)

        ns_bf = [r.n for r in sec.results if r.t_brute is not None and not r.timed_out]
        ts_bf = [r.t_brute for r in sec.results if r.t_brute is not None and not r.timed_out]
        if ns_bf:
            ax.plot(ns_bf, ts_bf, "o-", color="tab:red",
                    label="brute-force", markersize=6)

        ns_to = [r.n for r in sec.results if r.timed_out]
        ts_to = [r.t_brute for r in sec.results if r.timed_out]
        if ns_to:
            ax.plot(ns_to, ts_to, "x", color="tab:red",
                    markersize=10, markeredgewidth=2, label="brute-force (timeout)")

        ax.set_xlabel("Number of qubits (n)")
        ax.set_ylabel("Runtime (seconds)")
        ax.set_title(sec.short_label, fontsize=11)
        ax.set_yscale("log")
        ax.legend(fontsize=8)
        ax.grid(True, alpha=0.3)

    for idx in range(n_plots, rows * cols):
        axes[idx // cols][idx % cols].set_visible(False)

    fig2.suptitle("PauLie vs brute-force Lie closure", fontsize=14, y=1.02)
    fig2.tight_layout()
    fig2.savefig("benchmark_comparison.pdf", dpi=150, bbox_inches="tight")
    fig2.savefig("benchmark_comparison.png", dpi=150, bbox_inches="tight")
    print("Saved: benchmark_comparison.pdf/.png", flush=True)


# ---------------------------------------------------------------------------
# Main
# ---------------------------------------------------------------------------

def main() -> None:
    """Run all benchmark sections and print results."""
    parser = argparse.ArgumentParser(description="Benchmark PauLie classification")
    parser.add_argument("--plot", action="store_true", help="Generate plots")
    args = parser.parse_args()

    print("=" * 80, flush=True)
    print(" Benchmark: brute-force Lie closure vs PauLie graph classification", flush=True)
    print("=" * 80, flush=True)

    sections = []

    sections.append(run_section(
        "[1] so(n) algebras (a1: [XY]) -- dim = n(n-1)/2",
        "so(n)",
        [(n, p(G_LIE["a1"], n=n)) for n in [3, 4, 5, 6, 7, 8]],
        bf_timeout=30.0,
    ))

    sections.append(run_section(
        "[2] so(2^n) algebras (a11: [XY, YX, YZ]) -- dim = 2^n(2^n-1)/2",
        "so(2^n)",
        [(n, p(G_LIE["a11"], n=n)) for n in [3, 4, 5, 6, 7]],
        bf_timeout=15.0,
    ))

    sections.append(run_section(
        "[3] su(2^n) algebras (a12: [XX, XY, YZ]) -- dim = 4^n - 1",
        "su(2^n)",
        [(n, p(G_LIE["a12"], n=n)) for n in [3, 4, 5, 6, 7]],
        bf_timeout=15.0,
    ))

    sections.append(run_section(
        "[4] su+su algebras (a13: [XX, YY, YZ]) -- two direct summands",
        "su+su",
        [(n, p(G_LIE["a13"], n=n)) for n in [3, 4, 5, 6, 7]],
        bf_timeout=15.0,
    ))

    sections.append(run_section(
        "[5] sp algebras (a9: [XY, XZ]) -- dim grows exponentially",
        "sp(2^(n-2))",
        [(n, p(G_LIE["a9"], n=n)) for n in [3, 4, 5, 6, 7]],
        bf_timeout=15.0,
    ))

    sections.append(run_section(
        "[6] so(2n-1) algebras (a8: [XX, XZ]) -- dim = (2n-1)(2n-2)/2",
        "so(2n-1)",
        [(n, p(G_LIE["a8"], n=n)) for n in [3, 4, 5, 6, 7, 8]],
        bf_timeout=30.0,
    ))

    sections.append(run_section(
        "[7] Heisenberg model: XX+YY couplings + Z fields -> so(2n)",
        "Heisenberg",
        [(n, make_heisenberg_generators(n))
         for n in [3, 4, 5, 6, 7, 8, 10, 15, 20, 50, 100]],
        bf_timeout=30.0,
        bf_max_n=8,
    ))

    # Random Hamiltonians need inline handling for the custom bf_max_n logic
    for locality, bf_cutoff in [(2, 7), (3, 6)]:
        sec_num = 7 + locality - 1
        label = f"random {locality}-local"
        sec = BenchmarkSection(
            title=f"[{sec_num}] Random {locality}-local Pauli Hamiltonians (seed=42)",
            short_label=label,
        )
        print(f"\n{sec.title}", flush=True)
        print_header()

        n_terms_factor = 4 - locality
        for n in [4, 5, 6, 7, 8, 10, 15, 20]:
            generators = make_random_pauli_hamiltonian(
                n, n_terms_factor * n, locality=locality, seed=42)

            t0 = time.perf_counter()
            algebra = generators.get_algebra()
            dla_dim = generators.get_dla_dim()
            t_paulie = time.perf_counter() - t0

            t_brute = None
            timed_out = False
            if n <= bf_cutoff:
                t0 = time.perf_counter()
                try:
                    dla = lie_closure_brute_force(generators.get(), timeout=30.0)
                    t_brute = time.perf_counter() - t0
                    bf_str = f"{format_time(t_brute)} ({len(dla)})"
                    speedup = t_brute / t_paulie if t_paulie > 1e-9 else float("inf")
                    speedup_str = f"{speedup:.0f}x"
                except _BruteForceTimeout:
                    t_brute = time.perf_counter() - t0
                    timed_out = True
                    bf_str = "TIMEOUT"
                    speedup_str = f">{t_brute / t_paulie:.0f}x"
            else:
                bf_str = "INFEASIBLE"
                speedup_str = "-"

            sec.results.append(BenchmarkResult(
                n=n, algebra=algebra, dla_dim=dla_dim,
                t_paulie=t_paulie, t_brute=t_brute, timed_out=timed_out,
            ))
            print(
                f"  {n:>3}  {algebra:>20}  {dla_dim:>8}  "
                f"{format_time(t_paulie):>10}  {bf_str:>12}  {speedup_str:>10}",
                flush=True,
            )
        sections.append(sec)

    print("\n" + "=" * 80, flush=True)
    print(" PauLie classifies in polynomial time regardless of algebra dimension.", flush=True)
    print(" Brute-force scales with DLA size -- infeasible for su/sp algebras.", flush=True)
    print("=" * 80, flush=True)

    if args.plot:
        plot_results(sections)


if __name__ == "__main__":
    main()
